     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $this->validate($request,['firstname'=>'required','lastname'=>'required','email'=>'required','password'=>'required']
        );
        $login =new login();
        $login->firstname=$request->input('firstname');
        $login->lastname=$request->input('lastname');
        $login->email=$request->input('email');
        $login->password=$request->input('password');
        $login->save();
        return redirect('logins/create')->with('success','Registered Successfully');
    }

